package operatori_prinitia_resheniy;

public class Operator_if_else_if {
    public static void main(String[] args) {
        int x =40;
        if (x == 10){
            System.out.println("Значение Х = 10");
        }else if (x ==20){
            System.out.println("Значение Х = 20");
        }else if (x==30){
            System.out.println("Значение Х = 30");
        }else {
            System.out.println("Это опаратор else");
        }
    }
}
