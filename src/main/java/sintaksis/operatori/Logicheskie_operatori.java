package sintaksis.operatori;

public class Logicheskie_operatori {

    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        System.out.println("a && b = "+(a&&b));
        System.out.println("a || b = "+(a||b));
        System.out.println("!(a && b) = "+!(a&&b));



    }

}
